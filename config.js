function add(a, b, c) {
    //all goRunner input arguments to javascript are strings
    return parseFloat(a) + parseFloat(b) + parseFloat(c); //so cast to float before adding
  }

function sub(a, b) {
    //all goRunner input arguments to javascript are strings
    return parseFloat(a) - parseFloat(b); //so cast to float before subtracting
  }
